@Injectable({ providedIn: 'root' })
export class TranslationService {
  readonly currentLanguage = signal<DemoLanguage>(this.readInitialLanguage());

  readonly languages: { code: DemoLanguage; labelKey: string }[] = [
    { code: 'en', labelKey: 'lang.en' },
    { code: 'es', labelKey: 'lang.es' },
  ];

  setLanguage(language: DemoLanguage): void {
    this.currentLanguage.set(language);
    localStorage.setItem(STORAGE_KEY, language);
    document.documentElement.lang = language;
  }

  get(key: string, params: Record<string, string | number> = {}): string {
    const value = TRANSLATIONS[this.currentLanguage()][key] ?? key;
    return Object.entries(params).reduce(
      (text, [param, replacement]) => text.replaceAll(`{${param}}`, String(replacement)),
      value,
    );
  }

  private readInitialLanguage(): DemoLanguage {
    const stored = localStorage.getItem(STORAGE_KEY);
    const language: DemoLanguage = stored === 'es' ? 'es' : 'en';
    document.documentElement.lang = language;
    return language;
  }
}
